package com.example.miprimeraappcompose.sintaxis

fun main(){
    val num1 = 5
    val num2 = 10

    //Suma de dos números e impresión por pantalla
    val suma = num1 + num2
    println(suma)

    //Resta de dos números e impresión por pantalla
    val resta = num1 - num2
    println(resta)

    //Multiplicación de dos números e impresión por pantalla
    val multiplicacion = num1 * num2
    println(multiplicacion)

    //División de dos número e impresión por pantalla
    val division = num1 / num2
    println(division)

    //Módulo de dos números e impresión por pantalla
    val modulo = num1 % num2
    println(modulo)
}