package com.example.miprimeraappcompose.sintaxis

fun main(){
   val num1 = 5
   val num2 = 10

    //¿Son estos dos valores iguales? "=="
   val isEqual = num1 == num2
   println(isEqual)

    //¿Son estos dos valores distintos? "!="
    val isNotEqual = num1 != num2
    println(isNotEqual)

    //¿Es un número mayor que el otro? ">"
    val isGreater = num1 > num2
    println(isGreater)

    //¿Es un número menor que el otro? "<"
    val isLess = num1 < num2
    println(isLess)


}